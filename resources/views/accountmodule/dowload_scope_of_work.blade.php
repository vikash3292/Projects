                        <li>
                            The parameters to be monitored shall be SPM, RPM, NOX, SO2, CO, HC (methane & non-methane),
                            and
                            fluorides and other parameters depending upon type of project.
                        </li>
                        <li>
                            Monitoring will be conducted at identified locations for one month, as per MoEF guidelines.
                        </li>
                    </ul>

                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <i><b>Meteorological Data Collection</b></i>
                </td>
            </tr>
