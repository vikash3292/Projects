                    <i><b>Soil Environment</b></i>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <ul>
                        <li>Soil sampling will be carried at various locations in the study area. The location of
                            sampling points will be decided after reconnaissance survey as per the requirement of MoEF
                            guidelines. Sample will be drawn from both agriculture and non-agriculture land.</li>
                        <li>
                            Impact of proposed project on soil characteristics and agricultural productivity shall also
                            be determined.
                        </li>
                    </ul>
                </td>
            </tr>
