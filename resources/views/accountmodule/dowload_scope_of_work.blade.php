                    The site visits will be undertaken to familiarize with local environmental conditions in and around
                    the proposed project. This will also help to design the monitoring network and plan field surveys to
                    estimate the current spatial and temporal patterns of ambient concentrations as well as pollutants
                    being discharged (from other sources) in air, water and on land within the study area. Additional
                    information will also be collected for the major noise sources, degraded/reclaimed land areas, flora
                    and fauna, topography, drainage, etc.
                </td>
