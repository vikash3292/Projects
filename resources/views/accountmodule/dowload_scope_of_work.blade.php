                <td style="padding-top: 10px;">
                    Meteorological data for parameters such as temperature, relative humidity, atmospheric pressures,
                    cloud cover and rainfall shall be recorded at project site. Wind speed and wind direction, which are
                    required for developing wind rose and doing dispersion modeling, shall also be recorded. The primary
                    data generated from site will be substantiated with data collected from nearest IMD.
                </td>
            </tr>
