                <td style="padding-top: 10px;">
                    Environmental Impact Assessment (EIA) and Environmental Management Plan (EMP) is an important and
                    essential technique for ensuring that the likely effects of new development on the environment are
                    fully understood and taken into account before the development is allowed to go ahead.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    While the Government attaches high priority to the formulation and timely execution of development
                    activities consistent with the declared socio-economic objectives of State’s Policy Government also
                    considers it essential that such development does not result in indiscriminate and destructive uses
                    of natural and other resources, nor should it lead to adverse environmental consequences at present
                    and in the future.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    The term EIA describes a technique and a process by which information about the environmental
                    effects of a project is collected by the Project Proponent (PP) from various sources and taken into
                    account by the Concerned Authority (CA) in forming their judgment on whether the proposed
                    development should go ahead. The CA also obtains from PP such additional information as may be
                    considered necessary to decide on the proposed development, especially those affecting the
                    environment, which should form the material considerations for a proper decision. </td>
            </tr>
            <tr>
