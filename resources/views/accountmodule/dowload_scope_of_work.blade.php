                <td style="font-weight: 600;padding-top: 10px;">Methodology for EIA </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">The methodology for conducting EIA study will be as under: </td>
            </tr>
            <tr>
                <td style="font-weight: 600;padding-top: 10px;">Reconnaissance Survey and Secondary Data Collection</td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">A technical team of M/s Grass Roots Research & Creation Pvt. Ltd. (GRC)
                    will visit the site to establish baseline monitoring locations of sensitive receptors (including
                    residences/ villages/ ecologically sensitive areas, etc.) in relation to the project site and
                    designated impact zones. The existing environmental condition in and around proposed site with
                    regard to air, water, soil, noise, sediment, water bodies, land use pattern, socio- economics,
                    traffic density, flora, fauna, zooplanktons, phytoplanktons etc. depending up on project
                    requirement, shall be ascertained.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    During the proposed EIA study, GRC team will collect all the relevant primary data and secondary
                    information from different sources including central/ state government departments and research
                    institutions etc. Data collection also involves identifying and collating historical published
                    materials, topographic maps, documents or discussions with project engineers, govt. officials and
                    project-affected people (if any). Project details from client will also be obtained for further
                    reference during the study. In addition, the study would comprise of assessment of the existing
                    status of various environmental parameters such as air, water, noise, soil, land use, ecological
                    (flora & fauna), socio-economic and cultural environments from published records.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <u><i>Land use Environment</i></u>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    The data collection for land environment would include the following:
                    <ul>
                        <li>Land-use pattern around the site with respect to Total Irrigated Land, Un-irrigated Land,
                            Cultivable Waste Land, Area Not Available for Cultivation, Forest Land, and Total Geological
                            Area. </li>
                        <li>The temporal changes in land use pattern due to proposed project would also be determined.
                        </li>
                    </ul>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <u><i>Biological Environment</i> </u>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">Reconnaissance survey will be conducted to study the terrestrial and
                    aquatic environment of the
                    impact zone. Secondary data will also be collected from published sources such as Botanical and
                    Zoological Survey of India (BSI and ZSI) offices and will be supplemented by field observations.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <u><i>Socio-Economic Environment </i> </u>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">Study on Socio-economic environment will be carried out as per the
                    project sitting. The following
                    information will be collected through secondary sources: </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">Demographic Characteristics: </td>
            </tr>
            <tr>
                <td>
                    <ul style="list-style: decimal;">
                        <li style="padding-top: 10px;">The ethnic composition of population, break-up of population
                            according to various age
                            groups, male: female ratio, educational level and literacy rate, occupational structure,
                            etc.</li>
                        <li style="padding-top: 10px;">
                            Health Status: Data on human health shall be collected from District Civil Hospital/Primary
                            Health Centers. Incidents of endemic and epidemic disease in the area will be noted.
                        </li>
                        <li style="padding-top: 10px;">
                            Basic Amenities: Information will be collected on drinking water resources and water supply,
                            medical facilities, sanitation facilities, recreational facilities, transportation,
                            communication and power supply.
                        </li>
                        <li style="padding-top: 10px;">
                            Sensitive Areas: All sensitive location around the project area with respect to Protected
                            Areas notified under the Wild Life (Protection) Act, 1972, Notified Eco-sensitive areas,
                            archaeological, Historical and ancient monuments etc will be identified.
                        </li>
                        <li style="padding-top: 10px;">
                            Miscellaneous: Information will also be collected on major industrial and commercial
                            activities, critically polluted areas as notified by the Central Pollution Control Board,
                            recreational activities etc.
                        </li>
                    </ul>
                </td>
            </tr>
