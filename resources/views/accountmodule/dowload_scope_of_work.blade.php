                    <i><b>Noise Environment</b></i>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    Noise levels will be monitored in and around proposed project area and inhabited areas where there
                    is a possibility of increasing its level. Separate day and night time noise levels shall be
                    recorded. Values of L10, L50, L90, Leq, Lday, Lnight and Ldn shall be computed.
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    <i><b>Water Environment</b></i>
                </td>
            </tr>
            <tr>
                <td style="padding-top: 10px;">
                    Water quality, for both surface and ground water, will be analyzed as per IS 2296, IS 10500. It is
                    expected to draw eight samples of surface & ground water for necessary investigation
                </td>
